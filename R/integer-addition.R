#' @name integer_addition
#' @export
#' @title adds 2 integers together
#' @description Addition of 2 integers for the purpose of returning a sum (the result of the addition of 2 integers)
#' @param a The first integer to add
#' @param b The second integer to add
integer_addition <- funtion(a, b){

  c <- a + b
  return(c)

}
